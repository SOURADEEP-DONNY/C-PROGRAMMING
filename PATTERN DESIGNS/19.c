#include<conio.h>
int main()
{
	int i,j,k=0,value;
	for(i=1;i<=9;i++)
	{
		i<6?k++:k--;
		value=1;
		for(j=1;j<=5;j++)
		{
			if(j>=6-k)
			{
				printf("%d",value);
				value++;
			}
			else
			{
				printf(" ");
			}
		}
	printf("\n");
	}
}
